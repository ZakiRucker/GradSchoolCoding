'''
2. Define a function greeting that accepts a person's name (string) and returns a greeting
message Hello, <name> where <name> is the value passed to the function. Use this
function to print the greeting message to "John", "Jack", and "Jill".
'''
def greeting(name):
    return "Hello, " + name

print(greeting("John"))
print(greeting("Jack"))
print(greeting("Jill"))
